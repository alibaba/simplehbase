package com.alipay.simplehbase.antlr.manual;

import java.util.Date;

/**
 * TimeStampRange.
 * 
 * @author xinzhi.zhang
 * */
public class TimeStampRange {

    private Date start;
    private Date end;

    public Date getStart() {
        return start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }
}
